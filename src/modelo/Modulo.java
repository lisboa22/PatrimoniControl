package modelo;

import java.util.Date;

/**
 *
 * @author robson
 */
public class Modulo {
    private int id;
    private String nome;
    private Date data;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public Date getData() {
        return data;
    }

    public void setData(Date data) {
        this.data = data;
    }

    @Override
    public String toString() {
        return "Modulo{" + "id=" + id + ", nome=" + nome + ", data=" + data + '}';
    }
    
    
}
